if booked:
    booked = [i.split(", ") for i in booked]
    for row, place in booked:
        row, place = int(row[2:]) - 1, int(place[2:]) - 1
        places[row][place] = 'X'
for i in range(5):
    places[i] = ['X' if j == 'X' else 'O' for j in places[i]]
    print(*places[i])
